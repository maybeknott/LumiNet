func nextPowerOfTwo(num uint64) uint64 {
	num++
	var powerOfTwo uint64 = 1
	for (uint64(1) << powerOfTwo) < num {
		powerOfTwo++
	}
	return uint64(1) << powerOfTwo
}

func bitCount(num uint64) uint64 {
	var bits uint64 = 0
	for (num >> bits) > 1 {
		bits++
	}
	return bits
}

// NewBlackRock initializes the BlackRock Feistel cipher
func NewBlackRock(rangeVal uint64, seed uint64, rounds uint32) *BlackRock {
	var a, b uint64

	a = nextPowerOfTwo(uint64(math.Sqrt(float64(rangeVal) * 1.0)))
	b = nextPowerOfTwo(rangeVal / a)

	br := &BlackRock{
		Range:  rangeVal,
		A:      a,
		ABits:  bitCount(a),
		AMask:  a - 1,
		B:      b,
		BBits:  bitCount(b),
		BMask:  b - 1,
		Rounds: rounds,
		Seed:   seed,
	}
	return br
}

func roundFunc(r uint64, R uint64, seed uint64) uint64 {
	T := R ^ ((seed >> r) | (seed << (64 - r)))
	var Y uint32

	if (r & 1) != 0 {
		Y = sb8[T&0x3F] ^ sb6[(T>>8)&0x3F] ^ sb4[(T>>16)&0x3F] ^ sb2[(T>>24)&0x3F]
	} else {
		Y = sb7[T&0x3F] ^ sb5[(T>>8)&0x3F] ^ sb3[(T>>16)&0x3F] ^ sb1[(T>>24)&0x3F]
	}
	return uint64(Y)
}

func encrypt(r uint32, aBits uint64, aMask uint64, bBits uint64, bMask uint64, m uint64, seed uint64) uint64 {
	L := m & aMask
	R := m >> aBits
	var j uint32 = 1

	for j = 1; j <= r; {
		tmp := (L + roundFunc(uint64(j), R, seed)) & aMask
		L = R
		R = tmp
		j++

		tmp = (L + roundFunc(uint64(j), R, seed)) & bMask
		L = R
		R = tmp
		j++
	}

	if ((j - 1) & 1) != 0 {
		return (L << aBits) + R
	} else {
		return (R << aBits) + L
	}
}

func decrypt(r uint32, a uint64, b uint64, m uint64, seed uint64) uint64 {
	var L, R uint64
	var j int

	if (r & 1) != 0 {
		R = m % a
		L = m / a
	} else {
		L = m % a
		R = m / a
	}

	for j = int(r); j >= 1; j-- {
		if (j & 1) != 0 {
			tmp := roundFunc(uint64(j), L, seed)
			if tmp > R {
				tmp = tmp - R
				tmp = a - (tmp % a)
				if tmp == a {
					tmp = 0
				}
			} else {
				tmp = (R - tmp) % a
			}
			R = L
			L = tmp
		} else {
			tmp := roundFunc(uint64(j), L, seed)
			if tmp > R {
				tmp = tmp - R
				tmp = b - (tmp % b)
				if tmp == b {
					tmp = 0
				}
			} else {
				tmp = (R - tmp) % b
			}
			R = L
			L = tmp
		}
	}
	return a*R + L
}

// Shuffle encrypts/shuffles the input index in a one-to-one range mapping
func (br *BlackRock) Shuffle(m uint64) uint64 {
	c := encrypt(br.Rounds, br.ABits, br.AMask, br.BBits, br.BMask, m, br.Seed)
	for c >= br.Range {
		c = encrypt(br.Rounds, br.ABits, br.AMask, br.BBits, br.BMask, c, br.Seed)
	}
	return c
}

// Unshuffle decrypts/unshuffles the input value back to the original index
func (br *BlackRock) Unshuffle(m uint64) uint64 {
	c := decrypt(br.Rounds, br.A, br.B, m, br.Seed)
	for c >= br.Range {
		c = decrypt(br.Rounds, br.A, br.B, c, br.Seed)
	}
	return c
}
