fn next_power_of_two(mut num: u64) -> u64 {
    num += 1;
    let mut power_of_two = 1;
    while (1u64 << power_of_two) < num {
        power_of_two += 1;
    }
    1u64 << power_of_two
}

fn bit_count(num: u64) -> u64 {
    let mut bits = 0;
    while (num >> bits) > 1 {
        bits += 1;
    }
    bits
}

impl BlackRock {
    /// Create a new BlackRock instance.
    pub fn new(range_val: u64, seed: u64, rounds: u32) -> Self {
        let a = next_power_of_two(((range_val as f64) * 1.0).sqrt() as u64);
        let b = next_power_of_two(range_val / a);

        Self {
            range: range_val,
            a,
            a_bits: bit_count(a),
            a_mask: a - 1,
            b,
            b_bits: bit_count(b),
            b_mask: b - 1,
            rounds,
            seed,
        }
    }

    fn round_func(&self, r: u64, rv: u64) -> u64 {
        let t = rv ^ self.seed.rotate_right(r as u32);
        let y = if (r & 1) != 0 {
            SB8[(t & 0x3F) as usize]
                ^ SB6[((t >> 8) & 0x3F) as usize]
                ^ SB4[((t >> 16) & 0x3F) as usize]
                ^ SB2[((t >> 24) & 0x3F) as usize]
        } else {
            SB7[(t & 0x3F) as usize]
                ^ SB5[((t >> 8) & 0x3F) as usize]
                ^ SB3[((t >> 16) & 0x3F) as usize]
                ^ SB1[((t >> 24) & 0x3F) as usize]
        };
        y as u64
    }

    fn encrypt_val(&self, m: u64) -> u64 {
        let mut l = m & self.a_mask;
        let mut r = m >> self.a_bits;
        let mut j = 1;

        while j <= self.rounds {
            let tmp = (l + self.round_func(j as u64, r)) & self.a_mask;
            l = r;
            r = tmp;
            j += 1;

            let tmp = (l + self.round_func(j as u64, r)) & self.b_mask;
            l = r;
            r = tmp;
            j += 1;
        }

        if ((j - 1) & 1) != 0 {
            (l << self.a_bits) + r
        } else {
            (r << self.a_bits) + l
        }
    }

    fn decrypt_val(&self, m: u64) -> u64 {
        let mut l;
        let mut r;

        if (self.rounds & 1) != 0 {
            r = m % self.a;
            l = m / self.a;
        } else {
            l = m % self.a;
            r = m / self.a;
        }

        let mut j = self.rounds as i32;
        while j >= 1 {
            if (j & 1) != 0 {
                let tmp = self.round_func(j as u64, l);
                let tmp = if tmp > r {
                    let diff = tmp - r;
                    let val = self.a - (diff % self.a);
                    if val == self.a {
                        0
                    } else {
                        val
                    }
                } else {
                    (r - tmp) % self.a
                };
                r = l;
                l = tmp;
            } else {
                let tmp = self.round_func(j as u64, l);
                let tmp = if tmp > r {
                    let diff = tmp - r;
                    let val = self.b - (diff % self.b);
                    if val == self.b {
                        0
                    } else {
                        val
                    }
                } else {
                    (r - tmp) % self.b
                };
                r = l;
                l = tmp;
            }
            j -= 1;
        }
        self.a * r + l
    }

    /// Shuffles index `m` into a unique index in `0..self.range`
    pub fn shuffle(&self, m: u64) -> u64 {
        let mut c = self.encrypt_val(m);
        while c >= self.range {
            c = self.encrypt_val(c);
        }
        c
    }

    /// Unshuffles `m` back to the original index.
    pub fn unshuffle(&self, m: u64) -> u64 {
        let mut c = self.decrypt_val(m);
        while c >= self.range {
            c = self.decrypt_val(c);
        }
        c
    }
}
